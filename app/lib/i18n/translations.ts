const normalizeSpecialty = (value: string): string =>
  value
    .trim()
    .normalize('NFD')
    .replace(/[\u0300-\u036f]/g, '')
    .toUpperCase()
    .replace(/[^A-Z0-9]+/g, '_')
    .replace(/^_+|_+$/g, '');

const translateSpecialtyName = (name: string, lang: SupportedLang): string => {
  if (!name) return '';
  const normalized = normalizeSpecialty(name);
  const key = SPECIALTY_ALIASES[normalized] || normalized;
  return SPECIALTY_LABELS[lang][key] || name;
};

