// Inline SVG icon components — no external icon library needed.
// All icons accept optional `size` (default 16) and `className` props.

interface IconProps {
  size?: number;
  className?: string;
}

function icon(path: React.ReactNode, viewBox = '0 0 24 24') {
  return function Icon({ size = 16, className = '' }: IconProps) {
    return (
      <svg
        width={size}
        height={size}
        viewBox={viewBox}
        fill="none"
        stroke="currentColor"
        strokeWidth="2"
        strokeLinecap="round"
        strokeLinejoin="round"
        className={className}
        aria-hidden="true"
      >
        {path}
      </svg>
    );
  };
}

